public class Moto {
    int id;
    String fabricante;
    String cor;
    int ano;
    String placa;

    public Moto(int id, String fabricante, String cor, int ano, String placa) {
        this.id = id;
        this.fabricante = fabricante;
        this.cor = cor;
        this.ano = ano;
        this.placa = placa;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFabricante() {
        return fabricante;
    }

    public void setFabricante(String fabricante) {
        this.fabricante = fabricante;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }
    
    
}
